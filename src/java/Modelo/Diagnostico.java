package Modelo;

/**
 *
 * @author usuario
 */
public class Diagnostico {
    int id, idcita; 
    String descripcion;

    public Diagnostico() {
    }

    public Diagnostico(int id, int idcita, String descripcion) {
        this.id = id;
        this.idcita = idcita;
        this.descripcion = descripcion;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdcita() {
        return idcita;
    }

    public void setIdcita(int idcita) {
        this.idcita = idcita;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
     
}
